public enum CompareType {
    ALL("所有", "-1"), GREATER_EQUAL("大于等于", "greaterEqual"),LESS_EQUAL("小于等于", "lessEqual"),EQUAL ("等于", "equal"),BETWEEN ("介于", "between");
    // 成员变量
    private String name;
    private String code;
    public String getName() {
        return name;
    }
    public void setName(String name) {
        this.name = name;
    }
    public String getCode() {
        return code;
    }
    public void setCode(String code) {
        this.code = code;
    }
    private CompareType(String name, String code) {
        this.name = name;
        this.code = code;
    }
}
